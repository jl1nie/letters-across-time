export function getIntroduction(fromId: string): Introduction | null {
  return introductions[fromId] ?? null;
}

export function getIntroducedLetter(fromId: string): Letter | null {
  const intro = introductions[fromId];
  if (!intro) return null;
  return senpai.find((l) => l.id === intro.to) ?? null;
}
